// One-off generator — extracts Space Grotesk "8" and "Caps" outlines for the
// 8Caps logo. Run: node scripts/extract-logo-glyphs.mjs
// Not run at runtime or in CI; re-run only to regenerate the logo glyphs.
import opentypeModule from "opentype.js";
const opentype = opentypeModule.default ?? opentypeModule;
import { readdirSync, readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { join } from "node:path";

const FONT_DIR = "node_modules/@fontsource/space-grotesk/files";
const files = readdirSync(FONT_DIR);
const fontFile = files.find((f) => /latin-700-normal\.woff$/.test(f));
if (!fontFile) {
  throw new Error(
    `No 700-weight .woff in ${FONT_DIR}. Found: ${files.join(", ")}`,
  );
}
const font = opentype.parse(readFileSync(join(FONT_DIR, fontFile)).buffer);

const FS = 1000;

function translate(path, dx, dy) {
  for (const c of path.commands) {
    for (const k of ["x", "y", "x1", "y1", "x2", "y2"]) {
      if (c[k] !== undefined) c[k] += k[0] === "x" ? dx : dy;
    }
  }
}

const eight = font.getPath("8", 0, 0, FS);
const adv8 = font.getAdvanceWidth("8", FS);
const caps = font.getPath("Caps", adv8, 0, FS);

const b8 = eight.getBoundingBox();
const bC = caps.getBoundingBox();
const minX = Math.min(b8.x1, bC.x1);
const minY = Math.min(b8.y1, bC.y1);
const maxX = Math.max(b8.x2, bC.x2);
const maxY = Math.max(b8.y2, bC.y2);

translate(eight, -minX, -minY);
translate(caps, -minX, -minY);

const r = (n) => Math.round(n * 100) / 100;
const box = (b) => ({
  x: r(b.x1 - minX),
  y: r(b.y1 - minY),
  w: r(b.x2 - b.x1),
  h: r(b.y2 - b.y1),
});

const out = `// Generated by scripts/extract-logo-glyphs.mjs — do not edit by hand.
// Space Grotesk (SIL Open Font License) glyph outlines for the 8Caps logo.
export const EIGHT_PATH = ${JSON.stringify(eight.toPathData(2))};
export const CAPS_PATH = ${JSON.stringify(caps.toPathData(2))};
export const EIGHT_BOX = ${JSON.stringify(box(b8))} as const;
export const CAPS_BOX = ${JSON.stringify(box(bC))} as const;
export const LOCKUP_BOX = ${JSON.stringify({ w: r(maxX - minX), h: r(maxY - minY) })} as const;
`;

mkdirSync("src/components/brand", { recursive: true });
writeFileSync("src/components/brand/logo-glyphs.ts", out);
console.log("Wrote src/components/brand/logo-glyphs.ts");
console.log("EIGHT_BOX", box(b8));
